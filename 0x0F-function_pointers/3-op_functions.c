#include <stdlib.h>
/**
 * op_add - add two numbers
 * @a: number 1
 * @b: number 2
 * Return: the result of the adding
 */
int op_add(int a, int b)
{
	return ((a + b));
}
/**
 * op_sub - substract two numbers
 * @a: number 1
 * @b: number 2
 * Return: the result of the subtraccion
 */
int op_sub(int a, int b)
{
	return ((a - b));
}
/**
 * op_mul - multiplicate two numbers
 * @a: number 1
 * @b: number 2
 * Return: the result of the multiplication
 */
int op_mul(int a, int b)
{
	return ((a * b));
}
/**
 * op_div - divide two numbers
 * @a: number 1
 * @b: number 2
 * Return: the result of the divition
 */
int op_div(int a, int b)
{
	return ((a / b));
}
/**
 * op_mod - mod two numbers
 * @a: number 1
 * @b: number 2
 * Return: the result of the mod
 */
int op_mod(int a, int b)
{
	return ((a % b));
}
